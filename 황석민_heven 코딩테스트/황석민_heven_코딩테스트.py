dx=[ 0,1,0,-1]
dy=[-1,0,1,0]
def BackTracking(maze,ex,ey):
    foots=[]
    foots.append([ex,ey])
    foot = maze[ey][ex]
    while(True):
        for di in range(0,4):
            x = ex + dx[di]
            y = ey + dy[di]
            if(maze[y][x] == foot-4):
                foots.append([x,y])
                ex = x
                ey = y
                foot = maze[y][x]
                break
        if(foot == 1):
            foots.reverse()
            return foots

def LoadOfFlood(maze,sx,sy):
    queue = []
    queue.append([sx,sy])
    foot = maze[sy][sx]
    while(len(queue)>0):        
        sx,sy = queue.pop(0)
        foot = maze[sy][sx]
        for di in range(0,4):
           x = sx + dx[di]          
           y = sy + dy[di]
           if(maze[y][x]==2):
               maze[y][x]= foot+4
               return BackTracking(maze,x,y)               
           elif (maze[y][x]==0):
               maze[y][x]= foot+4
               queue.append([x,y])
    return []


